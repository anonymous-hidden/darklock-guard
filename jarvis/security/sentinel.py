    # ── Log Analysis ──────────────────────────────────────

    def _analyze_logs(self, svc: MonitoredService, scan_id: str):
        """Scan service logs for attack patterns."""
        attack_patterns = [
            (re.compile(r"(sql|injection|union\s+select|;\s*drop\s)", re.I),
             "SQL injection attempt", ThreatLevel.HIGH),
            (re.compile(r"\.\./\.\./|%2e%2e%2f|path\s+traversal", re.I),
             "Path traversal attempt", ThreatLevel.HIGH),
            (re.compile(r"<script|javascript:|onerror=|onload=", re.I),
             "XSS attempt detected", ThreatLevel.MEDIUM),
            (re.compile(r"(brute.?force|too\s+many\s+attempts|rate.?limit)", re.I),
             "Brute force / rate limit trigger", ThreatLevel.MEDIUM),
            (re.compile(r"(unauthorized|forbidden|invalid\s+token).*(admin|dashboard)", re.I),
             "Unauthorized admin access attempt", ThreatLevel.HIGH),
            (re.compile(r"(ECONNREFUSED|ETIMEDOUT|EHOSTUNREACH)", re.I),
             "Connection failure (possible network attack)", ThreatLevel.LOW),
        ]

        for log_rel in svc.log_paths:
            log_path = os.path.join(svc.base_dir, log_rel)
            if not os.path.isfile(log_path):
                continue
            try:
                # Read last 500 lines
                with open(log_path, "r", errors="replace") as f:
                    lines = f.readlines()[-500:]

                for pattern, desc, level in attack_patterns:
                    matches = [
                        line.strip() for line in lines
                        if pattern.search(line)
                    ]
                    if matches:
                        self._add_finding(SecurityFinding(
                            id=f"{scan_id}-{svc.name}-log-{desc[:20].replace(' ', '_')}",
                            timestamp=time.time(),
                            category="log",
                            threat_level=level,
                            service=svc.name,
                            title=f"{svc.name}: {desc}",
                            description=f"Found {len(matches)} matching log entries in {log_rel}.",
                            evidence={
                                "log_file": log_rel,
                                "match_count": len(matches),
                                "samples": matches[:3],
                            },
                        ))
            except Exception:
                pass

    # ── Config Analysis ───────────────────────────────────

    def _check_config_security(self, svc: MonitoredService, scan_id: str):
        """Check for insecure configuration patterns."""
        env_path = os.path.join(svc.base_dir, ".env")
        if os.path.isfile(env_path):
            try:
                with open(env_path, "r") as f:
                    content = f.read()

                # Check for weak/default secrets
                weak_patterns = [
                    (r"JWT_SECRET\s*=\s*(your_|change_me|secret|password|test)", "Weak JWT secret"),
                    (r"ADMIN_PASSWORD\s*=\s*(admin|password|123|test)", "Weak admin password"),
                    (r"API_KEY\s*=\s*(test|demo|change)", "Default API key"),
                ]
                for pat, desc in weak_patterns:
                    if re.search(pat, content, re.I):
                        self._add_finding(SecurityFinding(
                            id=f"{scan_id}-{svc.name}-config-{desc[:15].replace(' ', '_')}",
                            timestamp=time.time(),
                            category="config",
                            threat_level=ThreatLevel.HIGH,
                            service=svc.name,
                            title=f"{svc.name}: {desc}",
                            description=f"Detected a weak or default value in .env configuration.",
                            evidence={"file": ".env", "pattern": desc},
                            remediation="Generate a strong random secret and update .env.",
                        ))
            except Exception:
                pass

    # ── Cross-Service Analysis ────────────────────────────

    def _cross_service_analysis(self, scan_id: str):
        """Correlate findings across services for multi-stage attacks."""
        recent = [
            f for f in list(self._findings)
            if f.timestamp > time.time() - 300  # last 5 min
        ]

        # Pattern: multiple services have auth issues = coordinated probe
        auth_services = set(
            f.service for f in recent if f.category == "auth"
        )
        if len(auth_services) >= 2:
            self._add_finding(SecurityFinding(
                id=f"{scan_id}-cross-auth-probe",
                timestamp=time.time(),
                category="network",
                threat_level=ThreatLevel.CRITICAL,
                service="all",
                title="Multi-service authentication probe detected",
                description=(
                    f"Authentication issues found across {len(auth_services)} services "
                    f"({', '.join(auth_services)}). This may indicate a coordinated attack."
                ),
                evidence={"services": list(auth_services)},
                remediation="Review all auth configurations immediately.",
            ))

        # Auto-generate remediation programs for critical findings
        critical_unpatched = [
            f for f in recent
            if f.threat_level in (ThreatLevel.HIGH, ThreatLevel.CRITICAL)
            and not f.auto_remediated
        ]
        for finding in critical_unpatched:
            self._generate_remediation(finding)

    # ── Autonomous Program Generation ─────────────────────

    # ── Program caps ──────────────────────────────────────
    MAX_PROGRAMS_TOTAL = 20          # hard cap on all stored programs
    MAX_PER_SERVICE_TYPE = 1         # one active program per service+type slot

    def _program_exists_for(self, service: str, prog_type: str) -> bool:
        """Return True if an active (non-rejected) program already covers service+type."""
        with self._lock:
            for p in self._programs:
                if p.status == "rejected":
                    continue
                if p.service == service and p.category == prog_type:
                    return True
        return False

    def _trim_old_programs(self):
        """Keep only the most recent MAX_PROGRAMS_TOTAL programs, dropping oldest executed/rejected first."""
        with self._lock:
            if len(self._programs) <= self.MAX_PROGRAMS_TOTAL:
                return
            # Sort: keep pending/approved first, then by recency
            priority = {"generated": 0, "approved": 1, "failed": 2, "executed": 3, "rejected": 4}
            self._programs.sort(key=lambda p: (priority.get(p.status, 5), -p.created_at))
            removed = self._programs[self.MAX_PROGRAMS_TOTAL:]
            self._programs = self._programs[:self.MAX_PROGRAMS_TOTAL]
            # Clean up generated files for removed programs
            for p in removed:
                try:
                    if p.file_path and os.path.isfile(p.file_path):
                        os.remove(p.file_path)
                except Exception:
                    pass

    def _generate_remediation(self, finding: SecurityFinding):
        """Auto-generate a security program to fix the finding."""
        program = None

        title_lower = finding.title.lower()
        desc_lower = finding.description.lower()

        # ── Deduplication: one active program per service+type slot ──
        # Map finding to the program type Nova would generate
        if finding.category == "file" and "missing" in title_lower:
            # File-missing findings are low value — skip entirely
            finding.auto_remediated = True
            return
        elif finding.category == "network" and "not responding" in title_lower:
            if self._program_exists_for(finding.service, "network"):
                finding.auto_remediated = True
                return
        elif finding.category == "dependency":
            if self._program_exists_for(finding.service, "dependencies"):
                finding.auto_remediated = True
                return
        elif finding.category == "config":
            if self._program_exists_for(finding.service, "config"):
                finding.auto_remediated = True
                return
        elif finding.category == "file" and ("permission" in title_lower or "writable" in title_lower):
            if self._program_exists_for(finding.service, "permissions"):
                finding.auto_remediated = True
                return
        elif finding.category == "auth":
            if self._program_exists_for(finding.service, "auth"):
                finding.auto_remediated = True
                return
        elif finding.category == "log" and ("injection" in title_lower or "traversal" in title_lower or "xss" in title_lower or "brute" in title_lower):
            if self._program_exists_for(finding.service, "security"):
                finding.auto_remediated = True
                return
        elif finding.category == "security":
            if self._program_exists_for(finding.service, "security"):
                finding.auto_remediated = True
                return

        if finding.category == "file" and ("writable" in title_lower or "permission" in title_lower):
            fpath = finding.evidence.get("path", "")
            if fpath:
                program = self._gen_permission_fix(finding, fpath)

        elif finding.category == "config" and ("weak" in title_lower or "secret" in title_lower or "default" in title_lower):
            program = self._gen_secret_rotation(finding)

        elif finding.category == "dependency" and ("npm" in title_lower or "vulnerabilit" in title_lower or "audit" in desc_lower):
            program = self._gen_npm_fix(finding)

        elif finding.category == "auth" and ("without auth" in title_lower or "bypass" in title_lower or "unprotected" in title_lower or "no auth" in title_lower):
            program = self._gen_auth_hardening(finding)

        elif finding.category == "network" and ("not responding" in title_lower or "down" in title_lower or "unreachable" in title_lower):
            program = self._gen_service_restart(finding)

        elif finding.category == "log" and ("injection" in title_lower or "traversal" in title_lower or "xss" in title_lower or "brute" in title_lower):
            program = self._gen_waf_rule(finding)

        elif finding.category == "security" and ("header" in title_lower or "missing" in title_lower):
            program = self._gen_auth_hardening(finding)

        # Catch-all: generate a diagnostic + remediation program for anything unmatched
        if not program:
            program = self._gen_diagnostic_program(finding)

        if program:
            finding.auto_remediated = True
            finding.remediation_program = program.file_path
            self._total_programs += 1

            # Determine if safe to auto-execute
            safe_auto = False
            if program.language == "bash":
                code_lower = program.source_code.lower()
                # Safe bash operations Nova can run herself
                safe_keywords = [
                    "chmod", "npm audit", "stat ", "ls -la",
                    "echo ", "cat ", "head ", "tail ",
                    "ss -tlnp", "phase 1", "investigating",
                ]
                dangerous_keywords = [
                    "rm -rf", "rm -f", "drop table", "delete from",
                    "> /dev/", "dd if=", "mkfs", "format",
                ]
                has_safe = any(kw in code_lower for kw in safe_keywords)
                has_dangerous = any(kw in code_lower for kw in dangerous_keywords)
                if has_safe and not has_dangerous:
                    safe_auto = True
            elif program.language == "python":
                code_lower = program.source_code.lower()
                # Safe python: diagnostics, read-only operations
                dangerous_py = ["os.remove", "shutil.rmtree", "drop table", "subprocess.run"]
                if not any(kw in code_lower for kw in dangerous_py):
                    safe_auto = True

            if safe_auto:
                self._execute_program(program, auto=True)
            else:
                # Create an autonomous task for the agent to review & execute
                if self._agent:
                    try:
                        self._agent.create_task(
                            title=f"Security fix: {finding.title}",
                            trigger="sentinel_finding",
                            trigger_details=finding.to_dict(),
                            steps=[
                                {
                                    "action": "run_command",
                                    "args": {"command": f"cat {program.file_path}"},
                                    "description": f"Review generated program: {program.name}",
                                },
                                {
                                    "action": "run_command",
                                    "args": {"command": self._exec_cmd(program)},
                                    "description": f"Execute fix: {program.name}",
                                    "requires_approval": True,
                                },
                            ],
                            reasoning=f"Sentinel generated {program.name} to fix: {finding.description}",
                        )
                    except Exception:
                        pass  # Agent may not be ready yet

    def _exec_cmd(self, program: GeneratedProgram) -> str:
        """Build the execution command for a program."""
        if program.language == "bash":
            return f"bash {program.file_path}"
        elif program.language == "python":
            return f"python3 {program.file_path}"
        return f"cat {program.file_path}"

