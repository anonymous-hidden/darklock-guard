# ── Data Structures ────────────────────────────────────────

class ThreatLevel:
    INFO = "info"
    LOW = "low"
    MEDIUM = "medium"
    HIGH = "high"
    CRITICAL = "critical"


@dataclass
class SecurityFinding:
    """A single discovered security issue."""
    id: str
    timestamp: float
    category: str           # network, auth, file, dependency, log, config
    threat_level: str       # info / low / medium / high / critical
    service: str            # which darklock service
    title: str
    description: str
    evidence: dict = field(default_factory=dict)
    remediation: str = ""
    auto_remediated: bool = False
    remediation_program: str = ""   # filename of auto-generated fix
    acknowledged: bool = False

    def to_dict(self) -> dict:
        return {
            "id": self.id,
            "timestamp": self.timestamp,
            "category": self.category,
            "threat_level": self.threat_level,
            "service": self.service,
            "title": self.title,
            "description": self.description,
            "evidence": self.evidence,
            "remediation": self.remediation,
            "auto_remediated": self.auto_remediated,
            "remediation_program": self.remediation_program,
            "acknowledged": self.acknowledged,
        }


@dataclass
class GeneratedProgram:
    """A security program auto-generated by the sentinel."""
    id: str
    created_at: float
    trigger_finding_id: str     # which finding triggered this
    name: str
    description: str
    language: str               # python, bash, javascript
    source_code: str
    file_path: str              # where it was written
    status: str = "generated"   # generated, approved, executed, failed, rejected
    execution_output: str = ""
    approved_by: str = ""       # "auto" or "cayden"
    retry_count: int = 0
    max_retries: int = 3
    failure_diagnosis: str = ""  # Nova's analysis of what went wrong
    parent_program_id: str = "" # if this was generated as a fix for another program
    # ── Rich metadata ──
    reasoning: str = ""         # Why Nova built this program
    plan: list = None           # Step-by-step plan of what the program does
    management: str = ""        # How Nova manages/monitors this program
    success_summary: str = ""   # Human-readable summary of what was fixed
    version: int = 1            # Increments on improvement
    improvements: list = None   # History of improvements [{version, change, timestamp}]
    category: str = ""          # e.g. "permissions", "dependencies", "auth", "network", "config"
    service: str = ""            # which service this program targets

    def __post_init__(self):
        if self.plan is None:
            self.plan = []
        if self.improvements is None:
            self.improvements = []

    def to_dict(self) -> dict:
        return {
            "id": self.id,
            "created_at": self.created_at,
            "trigger_finding_id": self.trigger_finding_id,
            "name": self.name,
            "description": self.description,
            "language": self.language,
            "file_path": self.file_path,
            "status": self.status,
            "execution_output": self.execution_output[:500],
            "approved_by": self.approved_by,
            "source_lines": self.source_code.count("\n") + 1,
            "source_code": self.source_code,
            "retry_count": self.retry_count,
            "max_retries": self.max_retries,
            "failure_diagnosis": self.failure_diagnosis,
            "parent_program_id": self.parent_program_id,
            "reasoning": self.reasoning,
            "plan": self.plan,
            "management": self.management,
            "success_summary": self.success_summary,
            "version": self.version,
            "improvements": self.improvements,
            "category": self.category,
            "service": self.service,
        }


@dataclass
class ScanResult:
    """Result of a full security scan cycle."""
    id: str
    started_at: float
    finished_at: float = 0.0
    services_scanned: int = 0
    findings_count: int = 0
    critical_count: int = 0
    programs_generated: int = 0
    summary: str = ""

    def to_dict(self) -> dict:
        return {
            "id": self.id,
            "started_at": self.started_at,
            "finished_at": self.finished_at,
            "duration_seconds": round(self.finished_at - self.started_at, 2)
                if self.finished_at else None,
            "services_scanned": self.services_scanned,
            "findings_count": self.findings_count,
            "critical_count": self.critical_count,
            "programs_generated": self.programs_generated,
            "summary": self.summary,
        }


