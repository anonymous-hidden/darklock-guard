    def _npm_audit(self, svc: MonitoredService, scan_id: str):
        """Run npm audit and parse results."""
        try:
            result = subprocess.run(
                ["npm", "audit", "--json", "--production"],
                cwd=svc.base_dir,
                capture_output=True, text=True, timeout=30,
            )
            if result.returncode != 0 and result.stdout:
                try:
                    data = json.loads(result.stdout)
                    vulns = data.get("metadata", {}).get("vulnerabilities", {})
                    high = vulns.get("high", 0)
                    critical = vulns.get("critical", 0)
                    if high + critical > 0:
                        level = ThreatLevel.CRITICAL if critical > 0 else ThreatLevel.HIGH
                        self._add_finding(SecurityFinding(
                            id=f"{scan_id}-{svc.name}-npm-vulns",
                            timestamp=time.time(),
                            category="dependency",
                            threat_level=level,
                            service=svc.name,
                            title=f"{svc.name}: {high + critical} high/critical npm vulnerabilities",
                            description=(
                                f"npm audit found {critical} critical and {high} high "
                                f"severity vulnerabilities in production dependencies."
                            ),
                            evidence={"critical": critical, "high": high, "vulnerabilities": vulns},
                            remediation="Run: npm audit fix --force",
                        ))
                except json.JSONDecodeError:
                    pass
        except (subprocess.TimeoutExpired, FileNotFoundError):
            pass

