"""
Nova — Security Sentinel
==========================
Fully autonomous security oversight for every Darklock system.

Monitors:
  - Discord Bot (port 3001) — auth routes, session integrity, CSRF
  - Darklock Platform (port 3002) — admin access, download tampering, RBAC
  - Darklock Guard (guard-v2) — vault integrity, IPC socket auth
  - Darklock Secure Notes (darklock-notes) — sync server TLS, zero-knowledge
  - Darklock Secure Channel (secure-channel) — IDS (4100) + RLY (4101) E2E keys
  - Nova Backend (port 8950) — self-integrity, tool abuse, prompt injection

Capabilities:
  - Continuous network scanning of all service ports
  - Endpoint probing for auth bypass / misconfiguration
  - File permission auditing across all projects
  - Dependency vulnerability scanning (npm audit, cargo audit)
  - Log analysis for suspicious patterns (brute force, injection, path traversal)
  - Automatic program generation for detected threats
  - Full audit trail piped to the Activity Ledger + Command Center

Every action is logged. Every generated program is sandboxed and
requires Guardian approval before execution.
"""

